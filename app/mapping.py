"""
Сопоставление данных amoCRM с показателями отчёта «План-Факт».

ЭТО ГЛАВНЫЙ ФАЙЛ НАСТРОЙКИ ОТЧЁТА.

Здесь описано, откуда программа берёт каждый показатель:
  - какие воронки amoCRM соответствуют вашим проектам (Фитнес, Нутрициология);
  - какие поля сделки означают «деньги в кассу», «рассрочка», «предоплата»;
  - как определить «квал-лид» и «продажу»;
  - какие планы (цели) стоят на месяц.

Названия полей нужно будет подставить ваши — они видны в amoCRM
(Настройки → Поля сделки). Пока стоят значения по умолчанию,
подходящие для демо-режима.
"""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass
class MonthlyPlan:
    """План (цель) на месяц по проекту."""

    leads: int = 0          # план по лидам
    sales: int = 0          # план по продажам, шт
    revenue: float = 0.0    # план по выручке, руб


@dataclass
class ProjectMapping:
    """Настройка одного проекта (воронки amoCRM)."""

    name: str                       # как показывать: «Фитнес», «Нутрициология»
    pipeline_id: int                # id воронки в amoCRM
    # Планы по месяцам: ключ "ГГГГ-ММ" -> MonthlyPlan
    plans: dict[str, MonthlyPlan] = field(default_factory=dict)


# ---------------------------------------------------------------------------
#  Названия кастомных полей сделки в amoCRM.
#  Подставьте сюда точные названия из вашего amoCRM.
# ---------------------------------------------------------------------------
FIELD_CASH = "Деньги в кассу"          # фактически полученные деньги
FIELD_INSTALLMENT = "Рассрочка"        # сумма рассрочки от школы
FIELD_PREPAYMENT = "Предоплата"        # предоплата (бронь)
FIELD_MANAGER = "Менеджер"             # менеджер (если не берём из ответственного)
FIELD_SOURCE = "Источник"              # источник заявки
FIELD_QUALIFIED = "Квал"               # признак квал-лида (да/нет)


# ---------------------------------------------------------------------------
#  Как определить «квал-лид» и «продажу».
# ---------------------------------------------------------------------------
# Продажа = выигранная сделка (этап amoCRM с типом 142 «Успешно реализовано»).
# Квал-лид определяется по полю FIELD_QUALIFIED (значение в этом списке = да),
# либо по достижению определённых этапов (STATUS_QUALIFIED_IDS).
QUALIFIED_TRUE_VALUES = {"да", "yes", "1", "true", "квал"}
STATUS_QUALIFIED_IDS: set[int] = set()  # можно указать id этапов, с которых лид считается квал


# ---------------------------------------------------------------------------
#  Проекты (воронки). id воронок берутся из amoCRM (или из демо-данных).
# ---------------------------------------------------------------------------
PROJECTS: list[ProjectMapping] = [
    ProjectMapping(
        name="Фитнес",
        pipeline_id=102,
        plans={
            "2026-06": MonthlyPlan(leads=140, sales=15, revenue=800000),
            "2026-07": MonthlyPlan(leads=150, sales=12, revenue=500000),
        },
    ),
    ProjectMapping(
        name="Нутрициология",
        pipeline_id=101,
        plans={
            "2026-06": MonthlyPlan(leads=120, sales=14, revenue=700000),
            "2026-07": MonthlyPlan(leads=130, sales=13, revenue=650000),
        },
    ),
]


def get_project_by_pipeline(pipeline_id: int | None) -> ProjectMapping | None:
    for p in PROJECTS:
        if p.pipeline_id == pipeline_id:
            return p
    return None
